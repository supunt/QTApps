#include "logger.h"

logger* logger::_logWriter_ = nullptr;
//-----------------------------------------------------------------------------------------------------------------------------------------
logger::logger()
{
    _logWriter_ = new logger;
    _logFileLock(_logger_mtx_);
}
//-----------------------------------------------------------------------------------------------------------------------------------------
 void logger::log(QString log)
 {
    _logFileLock.lock();
    _logFileLock.unlock();
 }
 //-----------------------------------------------------------------------------------------------------------------------------------------
 void logger::logDebug(QString log)
 {
    _logFileLock.lock();
    _logFileLock.unlock();
 }
 //-----------------------------------------------------------------------------------------------------------------------------------------
 void logger::logError(QString log)
 {
    _logFileLock.lock();
    _logFileLock.unlock();
 }
//-----------------------------------------------------------------------------------------------------------------------------------------
